package com.qingjin.enumeration;

/**
 * 操作类型枚举类
 * 一般用于设置注解值
 */
public enum OperationType {
    /**
     * 时间更新操作
     */
    TIME_UPDATE,

    /**
     * 时间插入操作
     */
    TIME_INSERT,

    /**
     * 用户更新操作
     */
    USER_UPDATE,

    /**
     * 用户插入操作
     */
    USER_INSERT,

    /**
     * 用户时间更新操作
     */
    USERTIME_UPDATE,

    /**
     * 用户时间插入操作
     */
    USERTIME_INSERT,

}
